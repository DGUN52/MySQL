-- MySQL
SELECT 
    P.MEMBER_NAME
    , R.REVIEW_TEXT
    , DATE_FORMAT(R.REVIEW_DATE,'%Y-%m-%d') REVIEW_DATE
FROM
    MEMBER_PROFILE P
    , REST_REVIEW R
    , (
        SELECT MEMBER_ID
        FROM 
        (
            SELECT MEMBER_ID, RANK() OVER(ORDER BY COUNT(*) DESC) rnk
            FROM REST_REVIEW
            GROUP BY MEMBER_ID
        ) T
        WHERE rnk=1
    ) MOST
WHERE 
    P.MEMBER_ID = R.MEMBER_ID
    AND P.MEMBER_ID = MOST.MEMBER_ID
ORDER BY
    3 ASC,
    2 ASC
;
